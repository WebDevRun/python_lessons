print('–†–µ–∑—É–ª—å—Ç–∞—Ç –º–µ—Ç–æ–¥–∞ capitalize(): ' + 'python is FUN!'.capitalize())

# –ú–µ—Ç–æ–¥ lower() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ–ø–∏—é —Å—Ç—Ä–æ–∫–∏ –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
print('–†–µ–∑—É–ª—å—Ç–∞—Ç –º–µ—Ç–æ–¥–∞ lower(): ' + 'Hello World!'.lower())

# –ú–µ—Ç–æ–¥ upper() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫—É,
# –≤ –∫–æ—Ç–æ—Ä–æ–π –≤—Å–µ —Å–∏–º–≤–æ–ª—ã —É–∫–∞–∑–∞–Ω—ã –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ.
# –°–∏–º–≤–æ–ª—ã –∏ —Ü–∏—Ñ—Ä—ã –∏–≥–Ω–æ—Ä–∏—Ä—É—é—Ç—Å—è.
print('–†–µ–∑—É–ª—å—Ç–∞—Ç –º–µ—Ç–æ–¥–∞ upper(): ' + 'Hello my friends!'.upper())

# –ú–µ—Ç–æ–¥ swapcase() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫—É,
# –≤ –∫–æ—Ç–æ—Ä–æ–π –≤—Å–µ –ø—Ä–æ–ø–∏—Å–Ω—ã–µ –±—É–∫–≤—ã —è–≤–ª—è—é—Ç—Å—è —Å—Ç—Ä–æ—á–Ω—ã–º–∏, –∏ –Ω–∞–æ–±–æ—Ä–æ—Ç.
print('–†–µ–∑—É–ª—å—Ç–∞—Ç –º–µ—Ç–æ–¥–∞ swapcase(): ' + 'Hello My Name Is PETER'.swapcase())

# –ú–µ—Ç–æ–¥ encode(encoding=encoding, errors=errors) –∫–æ–¥–∏—Ä—É–µ—Ç —Å—Ç—Ä–æ–∫—É,
# –∏—Å–ø–æ–ª—å–∑—É—è —É–∫–∞–∑–∞–Ω–Ω—É—é –∫–æ–¥–∏—Ä–æ–≤–∫—É, –≥–¥–µ:
# encoding - c—Ç—Ä–æ–∫–∞, —É–∫–∞–∑—ã–≤–∞—é—â–∞—è –∏—Å–ø–æ–ª—å–∑—É–µ–º—É—é –∫–æ–¥–∏—Ä–æ–≤–∫—É
# (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π; –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = UTF-8)
# errors - c—Ç—Ä–æ–∫–∞, —É–∫–∞–∑—ã–≤–∞—é—â–∞—è –º–µ—Ç–æ–¥ –æ—à–∏–±–∫–∏ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π).
# –ó–Ω–∞—á–µ–Ω–∏—è–º–∏ –º–æ–≥—É—Ç –±—ã—Ç—å:
# 'backslashreplace' - –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –æ–±—Ä–∞—Ç–Ω—É—é –∫–æ—Å—É—é —á–µ—Ä—Ç—É –≤–º–µ—Å—Ç–æ —Å–∏–º–≤–æ–ª–∞,
# –∫–æ—Ç–æ—Ä—ã–π –Ω–µ —É–¥–∞–ª–æ—Å—å –∑–∞–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å
# 'ignore' - –∏–≥–Ω–æ—Ä–∏—Ä—É–µ—Ç —Å–∏–º–≤–æ–ª—ã, –∫–æ—Ç–æ—Ä—ã–π –Ω–µ —É–¥–∞–ª–æ—Å—å –∑–∞–∫–æ–¥–∏—Ä–æ–≤–∞—Ç—å
# 'namereplace' - –∑–∞–º–µ–Ω—è–µ—Ç —Å–∏–º–≤–æ–ª —Ç–µ–∫—Å—Ç–æ–º, –æ–±—ä—è—Å–Ω—è—é—â–∏–º —ç—Ç–æ—Ç —Å–∏–º–≤–æ–ª
# 'strict' - –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –ø—Ä–∏ —Å–±–æ–µ –≤–æ–∑–Ω–∏–∫–∞–µ—Ç –æ—à–∏–±–∫–∞
# 'replace' - –∑–∞–º–µ–Ω—è–µ—Ç —Å–∏–º–≤–æ–ª –∑–Ω–∞–∫–æ–º –≤–æ–ø—Ä–æ—Å–∞
