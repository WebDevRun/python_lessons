# value - —Å—Ç—Ä–æ–∫–∞ –¥–ª—è –ø–æ–∏—Å–∫–∞ (–æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç)
# start - –ø–æ–∑–∏—Ü–∏—è –Ω–∞—á–∞–ª–∞ –ø–æ–∏—Å–∫–∞ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç; —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ;
# –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = 0)
# end - –ø–æ–∑–∏—Ü–∏—è –∫–æ–Ω—Ü–∞ –ø–æ–∏—Å–∫–∞ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç, —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ;
# –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = –¥–ª–∏–Ω–∞ —Å—Ç—Ä–æ–∫–∏)
