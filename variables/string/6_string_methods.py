# –ú–µ—Ç–æ–¥ split() —Ä–∞–∑–±–∏–≤–∞–µ—Ç —Å—Ç—Ä–æ–∫—É –Ω–∞ –ø–æ–¥—Å—Ç—Ä–æ–∫–∏ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫,
# –µ—Å–ª–∏ –Ω–∞—Ö–æ–¥–∏—Ç —ç–∫–∑–µ–º–ø–ª—è—Ä—ã —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—è, –≥–¥–µ:
# separator - —É–∫–∞–∑—ã–≤–∞–µ—Ç —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å, –∏—Å–ø–æ–ª—å–∑—É–µ–º—ã–π
# –ø—Ä–∏ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–∏ —Å—Ç—Ä–æ–∫–∏ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π; –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = –ø—Ä–æ–±–µ–ª)
# maxsplit - —É–∫–∞–∑—ã–≤–∞–µ—Ç, —Å–∫–æ–ª—å–∫–æ —Ä–∞–∑–¥–µ–ª–µ–Ω–∏–π –Ω—É–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å
# (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π; –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = -1, —á—Ç–æ –æ–∑–Ω–∞—á–∞–Ω–µ—Ç "–≤—Å–µ –≤—Ö–æ–¥–∂–µ–Ω–∏—è")
