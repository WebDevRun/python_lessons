# (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω—ã–π; –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é = -1, —á—Ç–æ –æ–∑–Ω–∞—á–∞–Ω–µ—Ç "–≤—Å–µ –≤—Ö–æ–¥–∂–µ–Ω–∏—è")
