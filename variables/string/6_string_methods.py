# –ú–µ—Ç–æ–¥ title() –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç—Ä–æ–∫—É, –≤ –∫–æ—Ç–æ—Ä–æ–π –ø–µ—Ä–≤—ã–π —Å–∏–º–≤–æ–ª
# –≤ –∫–∞–∂–¥–æ–º —Å–ª–æ–≤–µ –ø—Ä–æ–ø–∏—Å–Ω–æ–π.
# –ù–∞–ø—Ä–∏–º–µ—Ä, –∑–∞–≥–æ–ª–æ–≤–æ–∫ –∏–ª–∏ –∑–∞–≥–æ–ª–æ–≤–æ–∫.
# –ï—Å–ª–∏ —Å–ª–æ–≤–æ —Å–æ–¥–µ—Ä–∂–∏—Ç —Ü–∏—Ñ—Ä—É –∏–ª–∏ —Å–∏–º–≤–æ–ª, —Ç–æ –ø–µ—Ä–≤–∞—è –±—É–∫–≤–∞ –ø–æ—Å–ª–µ
